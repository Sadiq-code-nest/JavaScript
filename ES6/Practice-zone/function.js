//Creating a function

function add(num1, num2) {
    var result2 = num1 + num2;
    return result2;
}

function sub(num1, num2) {
    var result1 = num1 - num2;
    return result1;
}


function Mul(num1, num2) {
    var result3 = num1 * num2;
    return result3;
}

//Calling a function
var x = Mul(5, 6)
console.log(x);

var y = add(5, 6)
console.log(y);

var z = sub(5, 6)
console.log(z);


